package AulasIntrodutorias;

public class Arrays03 {
    public static void main(String[] args) {
        String[] nomes = new String[4];
        nomes[0] = "Goku";
        nomes[1] = "Vegetta";
        nomes[2] = "Gohan";
        nomes[3] = "Goten";
        for (int i = 0; i < nomes.length ; i++) {
            System.out.println(nomes[i]);

        }
    }
}
